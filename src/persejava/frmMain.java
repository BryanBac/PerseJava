    public void Aprender(){
        
        boolean aprendizaje = true;
        int salidaInt;
        while(aprendizaje){
            aprendizaje = false;
                for (int i = 0; i < 4; i++){
                    Integer esteDato[]  = datos.get(i).getValores();
                    Double salidaDoub=(esteDato[0]*pesos[0])+(esteDato[1]*pesos[1])+pesos[2];
                    if(salidaDoub>0){
                        salidaInt=1;
                    }
                    else{
                        salidaInt=0;
                    }
                    if(salidaInt!=esteDato[2]){
                        
                        pesos[0]=((Math.random()*1.0-(-1.0))) - ((Math.random()*1.0-(-1.0)));
                        pesos[1]=((Math.random()*1.0-(-1.0))) - ((Math.random()*1.0-(-1.0)));
                        pesos[2]=((Math.random()*1.0-(-1.0))) - ((Math.random()*1.0-(-1.0)));
                        this.TAaprendizaje.setText(TAaprendizaje.getText()+pesos[0].toString()+" # "+pesos[1].toString()+" # "+pesos[2].toString()+"\n");
                        aprendizaje=true;
                    }
                }
        }
    }
    
